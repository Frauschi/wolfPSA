static int expect_status(const char *label, psa_status_t status,
                         psa_status_t expected)
{
    if (status != expected) {
        printf("FAIL %s status=%d expected=%d\n", label, (int)status,
               (int)expected);
        return 1;
    }
    return 0;
}

/*
 * Import a public-key-only PQ key (LMS/HSS/XMSS) and return its id.
 * bits=0 requests automatic inference from the blob length.
 */
static int import_pq_pubkey(const char *label,
                            psa_key_type_t key_type,
                            psa_algorithm_t alg,
                            psa_key_bits_t bits,
                            const uint8_t *pub, size_t pub_len,
                            psa_key_id_t *key_id_out)
{
    psa_key_attributes_t attrs = psa_key_attributes_init();
    psa_status_t st;

    psa_set_key_type(&attrs, key_type);
    psa_set_key_algorithm(&attrs, alg);
    psa_set_key_usage_flags(&attrs, PSA_KEY_USAGE_VERIFY_MESSAGE);
    psa_set_key_bits(&attrs, bits);

    st = psa_import_key(&attrs, pub, pub_len, key_id_out);
    if (expect_status(label, st, PSA_SUCCESS) != 0) {
        return 1;
    }
    return 0;
}

